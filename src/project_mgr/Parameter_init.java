    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
       try {
           con=Connect.ConnectDB();
       
       if (pr_id.getText().equals("")) {
           JOptionPane.showMessageDialog( this, "Please enter Project ID","Error", JOptionPane.ERROR_MESSAGE);
           return;
            }
        if (no.getText().equals("")) {
           JOptionPane.showMessageDialog( this, "Please enter Valid Number of Parameters","Error", JOptionPane.ERROR_MESSAGE);
           return;
            }
       int num = Integer.parseInt(no.getText());
          if (num>30) {
           JOptionPane.showMessageDialog( this, "Please enter number less than 30.","Error", JOptionPane.ERROR_MESSAGE);
           return;
            } 
          else if (num<4) {
           JOptionPane.showMessageDialog( this, "Number of parameters should be more than 4.","Error", JOptionPane.ERROR_MESSAGE);
           return;
            }
        String pname;
         Statement stmt;
       stmt= con.createStatement();
       String sql="Select Name from Project where Prj_ID= '" + pr_id.getText() + "'";
      rs=stmt.executeQuery(sql);
      int nm = rs.getInt(1);
      if(rs.next()){
       pname=rs.getString(1);
        Parameter_setup frm=new Parameter_setup(nm,num);
     frm.setVisible(true);    
    
   }
      else
      {
       
      JOptionPane.showMessageDialog(this,"Parameter Setup couldn't be completed \n Please Contact DB Administrator","Error Initialising project",JOptionPane.ERROR_MESSAGE); 

      }
    }
      catch(HeadlessException | SQLException ex){
           JOptionPane.showMessageDialog(this,ex); 
                }
    }//GEN-LAST:event_submitActionPerformed

